    private int findBest(List<Recognition> list) {
        int index = -1;
        while (index == -1 && opModeIsActive()) {
            int minimum = 10000;
            Recognition minRecog = null;
            for (Recognition recognition : list) {
                if (recognition.getLeft() < minimum) {
                    minRecog = recognition;
                    minimum = (int) recognition.getLeft();
                }
            }
            if (minRecog.getLabel().equals("Skystone")) {
                int n = list.size();
                for (int i = 0; i < n; i ++) {
                    if (list.get(i) == minRecog) {
                        index = i;
                        break;
                    }
                }
            }
        }
        return index;
    }

